# Verb-existence guard (mirrors test_work_skill_consistency.py)
# ---------------------------------------------------------------------------


def _extract_planctl_verbs(skill_text: str) -> list[tuple[str, ...]]:
    """Extract every `planctl <verb>` invocation from fenced bash blocks.

    Lifted from `test_work_skill_consistency._extract_planctl_verbs` (same
    multi-word-prefix awareness, same dedupe). Returns sorted argv tuples.
    """
    verbs: set[tuple[str, ...]] = set()
    in_bash = False
    for line in skill_text.splitlines():
        stripped = line.strip()
        if stripped.startswith("```bash"):
            in_bash = True
            continue
        if stripped.startswith("```"):
            in_bash = False
            continue
        if not in_bash:
            continue
        for match in re.finditer(r"planctl\s+([\w-]+(?:\s+[\w-]+)*)", line):
            words = match.group(1).split()
            if not words:
                continue
            head = words[0]
            if head in _MULTIWORD_PREFIXES and len(words) >= 2:
                verbs.add((head, words[1]))
            else:
                verbs.add((head,))
    return sorted(verbs)


def _all_verbs() -> list[tuple[str, ...]]:
    """Union of verbs extracted from both rendered skills."""
    verbs: set[tuple[str, ...]] = set()
    for path in (_QUEUE_SKILL, _DEFER_SKILL):
        if path.is_file():
            verbs.update(_extract_planctl_verbs(path.read_text()))
    return sorted(verbs)


_VERBS: list[tuple[str, ...]] = _all_verbs()


def test_extracted_verbs_nonempty():
    """Sanity: at least one verb must surface in the union — the skills
    both embed `planctl scaffold` in a fenced bash block.
    """
    assert len(_VERBS) > 0, (
        "no planctl verbs extracted from queue/SKILL.md or defer/SKILL.md — "
        "either both files are missing or the fenced-bash extractor regressed"
    )


@pytest.mark.parametrize(
    "verb_parts",
    _VERBS,
    ids=lambda parts: "planctl-" + "-".join(parts),
)
def test_queue_defer_planctl_verbs_have_help(verb_parts: tuple[str, ...]):
    """Every `planctl <verb>` referenced in a fenced bash block of either
    rendered skill must respond to `--help` with exit code 0.

    Mirrors the fn-339 / fn-341 guard from `test_work_skill_consistency`.
    """
    result = CliRunner().invoke(cli, [*verb_parts, "--help"])
    assert result.exit_code == 0, (
        f"planctl {' '.join(verb_parts)} --help failed:\n{result.output}"
    )
